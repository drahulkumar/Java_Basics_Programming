public class Airthematic_operators {
    public static void main(String[] args){
        int a = 40;
        int b = 20;

        //Addition
        System.out.println(a+b);

        //Substraction
        System.out.println(a-b);

        //Multiplication
        System.out.println(a*b);

        //Division
        System.out.println(a/b);


        //Modulo
        System.out.println(a%b);

    }
}
